import math

x = 10

#  Binary Conversion
x = 0b10
print(bin(x))

# Hex Conversion
x = 0x12c
print(hex(x))

# Complex Numbers
x = 1 + 2j
print(x)

# Operators
x = 10 + 3
x = 10 - 3
x = 10 * 3
x = 10 / 3
x = 10 // 3  # Division Without Remainder
x = 10 % 3
x = 10 ** 3  # Exponent

# Augmented Assignment Operators
x += 1
x -= 1
x *= 1
x /= 1

# Useful Number Functions
PI: float = 3.14
print(round(PI))  # Round
print(abs(PI))  # Absolute Value

print(math.floor(PI))
